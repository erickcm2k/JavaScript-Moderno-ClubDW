// Define a function
function saludar(nombre) {
    console.log(`Hola, soy ${nombre}.`);
}

// // Define a function expression
const saludar2 = function(nombre) {
    console.log(`Hola, soy ${nombre}.`);
}

// Arrow function
 const saludar3 = (nombre) => {
     console.log(`Hola, soy ${nombre}.`);
 }

// saludar('Ricardo')
// saludar2('Ricardo')
saludar3('Ricardo')